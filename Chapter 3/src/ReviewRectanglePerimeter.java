public class ReviewRectanglePerimeter 
{

	public static void main(String[] args) 
	{
		int width = 4;
		int length =13;
		int perimeter;
		
		perimeter = 2*width + 2*length;
		System.out.println("Perimeter of  4x13 rectangle: " + perimeter);

	}

}

